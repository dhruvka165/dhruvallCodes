const month = 3

switch (month) {
    case 1: /// we can use anyother datatype also in case
        console.log("January");
        break;
    case 2:
        console.log("february");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        break;
    case 5:
        console.log("may");
        break;
    case 6:
        console.log("june");
        break;
    case 7:
        console.log("july");
        break;
    case 8:
        console.log("Augest");
        break;
    case 9:
        console.log("september");
        break;
    case 10:
        console.log("Octomber");
        break;
    case 11:
        console.log("November");
        break;
    case 12:
        console.log("December");
        break;

    default:
        console.log("Please enter number between 1 to 12 ");
        break;
}